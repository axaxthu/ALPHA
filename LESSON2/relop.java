package LESSON2;

public class relop {
    public static void main(String[] args) {
        int a=10;
        int b=5;
        System.out.println(a==b); //false
        System.out.println(a!=b); //true
        System.out.println(a>b); //true
        System.out.println(a<b); //false
        System.out.println(a>=b); //true
        System.out.println(a<=b); //false
    }
}
